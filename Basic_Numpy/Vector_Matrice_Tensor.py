import numpy as np

#Vector : Can be defined as represent ordered collections of numerical values endowed with both magnitude and directionality

Vector1=np.array([1,2,3]) #THIS IS A VECTOR 
print("\nVector 1 :\n",Vector1)

# Matrix : Can be defined as two-dimensional arrays of numerical values
Matrix1=np.array([[1,2,3],[4,5,6]])  #THIS IS A MATRIX
print("\nMatrix 1 :\n",Matrix1)

#Tensor : Can be defined as They are multidimensional arrays of numerical values which can constitute complex facts structures and relationships.

Tensor1=np.array([[[1,2],[3,4]],[[5,6],[7,8]]])
print("\nTensor 1 :\n",Tensor1)